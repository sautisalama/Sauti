// Category icons for navigation
const CATEGORY_ICONS: Record<string, string> = {
  'Smileys & People': '😀',
  'Hearts & Love': '❤️',
  'Animals & Nature': '🐶',
  'Food & Drink': '🍔',
  'Activities': '⚽',
  'Travel & Places': '🚗',
  'Objects': '💡',
  'Symbols': '🔣',
  'Flags': '🏳️'
};

interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  onClose: () => void;
}

export function EmojiPicker({ onEmojiSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState<keyof typeof EMOJI_DATA>('Smileys & People');
  const [searchQuery, setSearchQuery] = useState('');
  const categories = Object.keys(EMOJI_DATA) as (keyof typeof EMOJI_DATA)[];

  // Filter emojis based on search
  const filteredEmojis = useMemo(() => {
    if (!searchQuery.trim()) {
      return EMOJI_DATA[activeCategory];
    }
    // Search across all categories
    const allEmojis: string[] = [];
    Object.values(EMOJI_DATA).forEach(emojis => {
      allEmojis.push(...emojis);
    });
    // For now, return all emojis (emoji search by name would require a lookup table)
    // This is a simple implementation - emojis are returned as-is
    return allEmojis.slice(0, 200); // Limit to prevent performance issues
  }, [activeCategory, searchQuery]);

  return (
    <div className="bg-white rounded-2xl shadow-2xl border border-serene-neutral-200 w-full md:w-[400px] overflow-hidden animate-in fade-in slide-in-from-bottom-2 duration-200">
      {/* Search Header */}
      <div className="p-3 border-b border-serene-neutral-100 bg-serene-neutral-50/50">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-serene-neutral-400" />
          <input
            type="text"
            placeholder="Search emojis..."
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="w-full pl-9 pr-8 py-2 text-sm bg-white border border-serene-neutral-200 rounded-xl focus:outline-none focus:ring-2 focus:ring-serene-blue-200 focus:border-serene-blue-400 transition-all"
          />
          {searchQuery && (
            <button
              onClick={() => setSearchQuery('')}
              className="absolute right-3 top-1/2 -translate-y-1/2 text-serene-neutral-400 hover:text-serene-neutral-600"
            >
              <X className="h-4 w-4" />
            </button>
          )}
        </div>
      </div>

      {/* Category Navigation */}
      {!searchQuery && (
        <div className="flex border-b border-serene-neutral-100 px-2 py-2 gap-1 overflow-x-auto scrollbar-hide bg-white">
          {categories.map((cat) => (
            <button
              key={cat}
              onClick={() => setActiveCategory(cat)}
              className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-medium whitespace-nowrap transition-all ${
                activeCategory === cat
                  ? 'bg-serene-blue-100 text-serene-blue-700'
                  : 'text-serene-neutral-500 hover:bg-serene-neutral-100 hover:text-serene-neutral-700'
              }`}
              title={cat}
            >
              <span className="text-base">{CATEGORY_ICONS[cat]}</span>
              <span className="hidden sm:inline">{cat.split(' ')[0]}</span>
            </button>
          ))}
        </div>
      )}

      {/* Emoji Grid */}
      <div className="p-3 h-[280px] md:h-[240px] overflow-y-auto">
        <div className="grid grid-cols-8 md:grid-cols-10 gap-0.5">
          {filteredEmojis.map((emoji, idx) => (
            <button
              key={idx}
              onClick={() => {
                onEmojiSelect(emoji);
                onClose();
              }}
              className="w-10 h-10 md:w-9 md:h-9 flex items-center justify-center text-2xl md:text-xl hover:bg-serene-blue-50 rounded-lg transition-colors active:scale-90 active:bg-serene-blue-100"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>

      {/* Footer */}
      <div className="px-3 py-2 border-t border-serene-neutral-100 bg-serene-neutral-50/50 flex justify-between items-center">
        <span className="text-xs text-serene-neutral-400">
          {searchQuery ? 'Search results' : activeCategory}
        </span>
        <button
          onClick={onClose}
          className="text-xs text-serene-neutral-500 hover:text-serene-neutral-700 font-medium px-2 py-1 hover:bg-serene-neutral-100 rounded-md transition-colors"
        >
          Close
        </button>
      </div>
    </div>
  );
}
